        public static void SeedCharacters(WaifuBotContext context)
        {
            var characters = GetCharacters();

            foreach (var character in characters)
            {
                bool exists = context.Characters.Any(c => c.Name == character.Name && c.Series == character.Series);

                if (!exists)
                {
                    context.Characters.Add(character);
                }
            }

            context.SaveChanges();
        }
